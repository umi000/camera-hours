// SSE endpoint: browser connects and receives log events
app.get('/events', (req, res) => {
  res.setHeader('Content-Type', 'text/event-stream');
  res.setHeader('Cache-Control', 'no-cache');
  res.setHeader('Connection', 'keep-alive');
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.flushHeaders();

  const onMessage = (msg) => {
    res.write(`data: ${JSON.stringify(msg)}\n\n`);
  };

  emitter.on('message', onMessage);

  // send a ping every 20s to keep connection alive
  const keepAlive = setInterval(() => res.write(':\n\n'), 20000);

  req.on('close', () => {
    clearInterval(keepAlive);
    emitter.off('message', onMessage);
  });
});

// Endpoint to start tests
app.post('/run-tests', (req, res) => {
  if (running) {
    return res.status(409).json({ error: 'Tests already running' });
  }

  // Get selected clients and dates from request body
  const selectedClients = req.body.clients || [];
  const startDate = req.body.startDate || '';
  const endDate = req.body.endDate || '';
  const reportType = req.body.reportType || 'summary';
  const reportName = req.body.reportName || 'employee-camera-hours';
  
  if (selectedClients.length === 0) {
    return res.status(400).json({ error: 'No clients selected for testing' });
  }
  
  // Validate that all clients have required fields
  const validClients = selectedClients.filter(client => {
    if (!client.applandingurl) {
      console.error(`❌ Client "${client.name}" missing applandingurl`);
      return false;
    }
    if (!client.liveserverip) {
      console.error(`❌ Client "${client.name}" missing liveserverip`);
      return false;
    }
    if (!client.dbname) {
      console.error(`❌ Client "${client.name}" missing dbname`);
      return false;
    }
    return true;
  });
  
  if (validClients.length === 0) {
    return res.status(400).json({ error: 'No clients with valid URLs selected for testing' });
  }
  
  if (validClients.length !== selectedClients.length) {
    console.log(`⚠️ ${selectedClients.length - validClients.length} clients skipped due to missing URLs`);
  }

  running = true;
  res.status(202).json({ status: 'started' });

  // Delete any existing Excel file at the start of a new test run
  const excelFileName = 'report-data.xlsx';
  const excelFilePath = path.join(__dirname, excelFileName);
  if (fs.existsSync(excelFilePath)) {
    fs.unlinkSync(excelFilePath);
  }

  console.log(`🎭 Starting Playwright tests for ${validClients.length} clients...`);
  if (startDate && endDate) {
    console.log(`📅 Using dates from UI - Start: ${startDate}, End: ${endDate}`);
  }
  console.log(`📊 Report Type: ${reportType}, Report Name: ${reportName}`);
  emitter.emit('message', { type: 'info', text: `Starting Playwright tests for ${validClients.length} clients...\n` });

  // Set environment variables with validated clients and dates
  // Create a clean copy of process.env and remove color-related vars to prevent warnings
  const env = {
    ...process.env
  };
  
  // Remove color-related environment variables to prevent warnings
  delete env.FORCE_COLOR;
  delete env.NO_COLOR;
  
  // Add our custom environment variables
  env.SELECTED_CLIENTS = JSON.stringify(validClients);
  env.START_DATE = startDate;
  env.END_DATE = endDate;
  env.REPORT_TYPE = reportType;
  env.REPORT_NAME = reportName;

  // Use playwright directly from node_modules to avoid npx overhead
  // This is faster than using npx which has to resolve and execute playwright each time
  const playwrightPath = path.join(__dirname, 'node_modules', '.bin', process.platform === 'win32' ? 'playwright.cmd' : 'playwright');
  
  // On Windows with shell: true, we need to properly quote paths with spaces
  // Use __dirname for cwd to ensure we're in the right directory (handles paths with spaces)
  const workingDir = __dirname;
  
  // For Windows with shell: true, construct the full command as a string to handle spaces properly
  let child;
  if (process.platform === 'win32') {
    // On Windows, quote the path and construct full command string
    const quotedPath = playwrightPath.includes(' ') ? `"${playwrightPath}"` : playwrightPath;
    const fullCommand = `${quotedPath} test --reporter=list`;
    child = spawn(fullCommand, [], { 
      shell: true,
      cwd: workingDir,
      env: env
    });
  } else {
    // On Unix-like systems, use array format
    child = spawn(playwrightPath, ['test', '--reporter=list'], { 
      shell: false,
      cwd: workingDir,
      env: env
    });
  }

  child.stdout.on('data', (d) => {
    const text = d.toString();
    console.log(text);
    logToFile('INFO', text.trim());
    emitter.emit('message', { type: 'stdout', text });
  });

  child.stderr.on('data', (d) => {
    const text = d.toString();
    console.error('⚠️ STDERR:', text);
    logToFile('ERROR', `STDERR: ${text.trim()}`);
    emitter.emit('message', { type: 'stderr', text });
  });

  child.on('close', (code) => {
    console.log(`🎭 Playwright tests completed with exit code: ${code}`);
    if (code !== 0) {
      console.error(`❌ Tests failed with exit code: ${code}`);
    }
    emitter.emit('message', { type: 'exit', code });
    emitter.emit('message', { type: 'info', text: `\nTests completed with exit code: ${code}\n` });
    running = false;
  });

  child.on('error', (err) => {
    console.error('❌ Playwright process error:', err.message);
    logToFile('ERROR', `Playwright process error: ${err.message}`);
    emitter.emit('message', { type: 'error', text: err.message });
    running = false;
  });
});

// Endpoint to update client data from database
app.post('/update-client-data', async (req, res) => {
  if (dbUpdateRunning) {
    return res.status(409).json({ error: 'Database update already running' });
  }

  dbUpdateRunning = true;
  res.status(202).json({ status: 'started', message: 'Database update initiated' });

  console.log('🔄 Starting automatic database update...');
  
  // Set a timeout for the database update
  const updateTimeout = setTimeout(() => {
    if (dbUpdateRunning) {
      console.error('⏱️  Database update timeout (30 seconds)');
      dbUpdateRunning = false;
      emitter.emit('message', { 
        type: 'error', 
        text: '⏱️  Database update timeout. Check VPN/network access.\n' 
      });
    }
  }, 35000); // 35 seconds timeout
  
  fetchClientData()
    .then((clientsData) => {
      clearTimeout(updateTimeout);
      console.log(`✅ Database update completed. Updated ${clientsData.length} clients.`);
      dbUpdateRunning = false;
      emitter.emit('message', { 
        type: 'info', 
        text: `✅ Database update completed. ${clientsData.length} clients updated.\n` 
      });
    })
    .catch((error) => {
      clearTimeout(updateTimeout);
      console.error('❌ Database update failed:', error.message);
      dbUpdateRunning = false;
      // Don't emit error if it's just a connection issue (expected when no VPN)
      if (!error.message.includes('timeout') && !error.message.includes('ECONNREFUSED')) {
        emitter.emit('message', { 
          type: 'error', 
          text: `❌ Database update failed: ${error.message}\n` 
        });
      }
    });
});

// Endpoint to receive report data from tests
app.post('/report-data', (req, res) => {
  try {
    const { clientName, clientId, reportData, timestamp, htmlTable } = req.body;
    
    // Log request size for debugging
    const requestSize = JSON.stringify(req.body).length;
    const requestSizeMB = (requestSize / (1024 * 1024)).toFixed(2);
    console.log(`📦 Received request for ${clientName}: ${requestSizeMB} MB`);
    
    // Check if we have any data (even just headers)
    if (!reportData || !reportData.headers || reportData.headers.length === 0) {
      console.log(`⚠️  No data to save for ${clientName}. No headers found.`);
      res.json({ status: 'received', message: 'No data - No headers found' });
      return;
    }
    
    // Log warning if no rows but continue to save headers
    if (!reportData.rows || reportData.rows.length === 0) {
      console.log(`⚠️  ${clientName}: No data rows found, but saving headers anyway...`);
    }
    
    console.log(`📊 Received report data for ${clientName}: ${reportData.rowCount} rows, ${reportData.headers.length} columns`);
    const excelFileName = 'report-data.xlsx';
    const excelFilePath = path.join(__dirname, excelFileName);
    
    if (fs.existsSync(excelFilePath)) {
      fs.unlinkSync(excelFilePath);
    }
    
    const worksheetData = [reportData.headers];
    reportData.rows.forEach(row => {
      const rowData = reportData.headers.map(header => row[header] || '');
      worksheetData.push(rowData);
    });
    
    const workbook = XLSX.utils.book_new();
    const worksheet = XLSX.utils.aoa_to_sheet(worksheetData);
    
    const maxWidth = 50;
    const colWidths = reportData.headers.map(header => {
      const headerLength = header.length;
      const maxDataLength = reportData.rows.reduce((max, row) => {
        return Math.max(max, String(row[header] || '').length);
      }, headerLength);
      return { wch: Math.min(maxWidth, Math.max(headerLength, maxDataLength) + 2) };
    });
    worksheet['!cols'] = colWidths;
    
    XLSX.utils.book_append_sheet(workbook, worksheet, 'Report Data');
    
    const metadataSheet = XLSX.utils.aoa_to_sheet([
      ['Client Name', clientName],
      ['Client ID', clientId || ''],
      ['Timestamp', timestamp || new Date().toISOString()],
      ['Total Rows', reportData.rowCount],
      ['Generated At', new Date().toLocaleString()]
    ]);
    XLSX.utils.book_append_sheet(workbook, metadataSheet, 'Metadata');
    
    XLSX.writeFile(workbook, excelFilePath);
    console.log(`✅ Excel file created: ${excelFileName} (${reportData.rowCount} rows)`);
    
    // Also save to JSON file in logs/json/portal directory
    const logsDir = path.join(__dirname, 'logs');
    if (!fs.existsSync(logsDir)) {
      fs.mkdirSync(logsDir, { recursive: true });
    }
    const jsonDir = path.join(logsDir, 'json', 'portal');
    if (!fs.existsSync(jsonDir)) {
      fs.mkdirSync(jsonDir, { recursive: true });
    }
    
    // Sanitize client name for filename
    const sanitizedClientName = clientName.replace(/[^a-zA-Z0-9]/g, '');
    const jsonFileName = `${sanitizedClientName}-report-${Date.now()}.json`;
    const jsonFilePath = path.join(jsonDir, jsonFileName);
    
    const jsonData = {
      clientName: clientName,
      clientId: clientId || '',
      timestamp: timestamp || new Date().toISOString(),
      generatedAt: new Date().toISOString(),
      totalRows: reportData.rowCount,
      headers: reportData.headers,
      data: reportData.rows
    };
    
    fs.writeFileSync(jsonFilePath, JSON.stringify(jsonData, null, 2), 'utf8');
    console.log(`✅ JSON file created: ${jsonFileName} (${reportData.rowCount} rows)`);
    console.log(`📁 JSON file path: ${jsonFilePath}`);
    
    // Save HTML table if provided
    if (htmlTable) {
      const htmlFileName = `${sanitizedClientName}-report-${Date.now()}.html`;
      const htmlFilePath = path.join(jsonDir, htmlFileName);
      const htmlContent = generateHtmlReport(clientName, clientId, reportData, timestamp, htmlTable);
      
      fs.writeFileSync(htmlFilePath, htmlContent, 'utf8');
      console.log(`✅ HTML file created: ${htmlFileName}`);
      console.log(`📁 HTML file path: ${htmlFilePath}`);
    }
    
    emitter.emit('message', { 
      type: 'info', 
      text: `📊 Excel file saved: ${excelFileName} (${reportData.rowCount} rows)\n📄 JSON file saved: ${jsonFileName} (${jsonFilePath})\n` 
    });
    
    emitter.emit('message', { 
      type: 'report-data', 
      clientName: clientName,
      clientId: clientId,
      reportData: reportData,
      timestamp: timestamp
    });
    
    res.json({ status: 'received', message: 'Report data received and saved to Excel and JSON', jsonFile: jsonFileName });
    
  } catch (error) {
    console.error('❌ Error saving Excel/JSON file:', error.message);
    logToFile('ERROR', `Error saving report data: ${error.message}`);
    emitter.emit('message', { 
      type: 'error', 
      text: `❌ Error saving report data: ${error.message}\n` 
    });
    res.status(500).json({ status: 'error', message: error.message });
  }
});

// In-memory storage for chunked data transfers
const chunkedDataStore = new Map();

// Cleanup stale sessions (older than 30 minutes)
setInterval(() => {
  const now = Date.now();
  const staleThreshold = 30 * 60 * 1000; // 30 minutes
  let cleaned = 0;
  
  for (const [sessionId, session] of chunkedDataStore.entries()) {
    if (now - session.startTime > staleThreshold) {
      chunkedDataStore.delete(sessionId);
      cleaned++;
      console.log(`🧹 Cleaned up stale chunked transfer session: ${sessionId}`);
    }
  }
  
  if (cleaned > 0) {
    console.log(`🧹 Cleaned up ${cleaned} stale chunked transfer session(s)`);
  }
}, 10 * 60 * 1000); // Run cleanup every 10 minutes

// Endpoint to initialize chunked data transfer
app.post('/report-data-chunk-start', (req, res) => {
  try {
    const { sessionId, clientName, clientId, headers, totalRows, timestamp } = req.body;
    
    if (!sessionId || !clientName || !headers) {
      return res.status(400).json({ status: 'error', message: 'Missing required fields' });
    }
    
    // Initialize chunk storage
    chunkedDataStore.set(sessionId, {
      clientName,
      clientId: clientId || '',
      headers,
      totalRows: totalRows || 0,
      timestamp: timestamp || new Date().toISOString(),
      rows: [],
      chunksReceived: 0,
      startTime: Date.now()
    });
    
    console.log(`📦 Chunked transfer initialized for ${clientName} (session: ${sessionId}, expected rows: ${totalRows})`);
    res.json({ status: 'initialized', sessionId, message: 'Chunked transfer initialized' });
  } catch (error) {
    console.error('❌ Error initializing chunked transfer:', error.message);
    res.status(500).json({ status: 'error', message: error.message });
  }
});

// Endpoint to receive a chunk of data
app.post('/report-data-chunk', (req, res) => {
  try {
    const { sessionId, chunkIndex, totalChunks, rows } = req.body;
    
    if (!sessionId || chunkIndex === undefined || !rows) {
      return res.status(400).json({ status: 'error', message: 'Missing required fields' });
    }
    
    const session = chunkedDataStore.get(sessionId);
    if (!session) {
      return res.status(404).json({ status: 'error', message: 'Session not found' });
    }
    
    // Append rows to session
    session.rows.push(...rows);
    session.chunksReceived++;
    
    console.log(`📦 Received chunk ${chunkIndex + 1}/${totalChunks} for ${session.clientName} (${rows.length} rows, total: ${session.rows.length})`);
    
    res.json({ 
      status: 'received', 
      chunkIndex, 
      totalChunks,
      rowsReceived: session.rows.length,
      message: `Chunk ${chunkIndex + 1}/${totalChunks} received` 
    });
  } catch (error) {
    console.error('❌ Error receiving chunk:', error.message);
    res.status(500).json({ status: 'error', message: error.message });
  }
});

