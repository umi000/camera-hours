      clientId: clientId,
      clientName: clientName,
      timestamp: new Date().toISOString(),
      runTimestamp: Date.now(),
      status: result.success ? 'success' : 'failed',
      fileName: result.fileName,
      filePath: result.filePath,
      stats: result.stats,
      differencesCount: result.stats?.differences || 0,
      dbOnlyCount: result.stats?.dbOnly || 0,
      portalOnlyCount: result.stats?.portalOnly || 0,
      matchingCount: result.stats?.matching || 0,
      totalDBRecords: result.stats?.totalDBRecords || 0,
